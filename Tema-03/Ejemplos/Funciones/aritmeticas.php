<?php

/** aritméticas.php
 * Contiene operaciones aritméticas.
 */

function sumar($var1, $var2) {
    return $var1 + $var2;
}

function restar($var1, $var2) {
    return $var1 - $var2;
}

function multiplicar($var1, $var2) {
    return $var1 * $var2;
}

function dividir($var1, $var2) {
    return $var1 / $var2;
}

function incremento(&$var) {
    $var++;
}

?>